#Prestar atenção nos parenteses e colchetes
#parenteses criam tuplas
#colchetes criam listas
cars = ['bmw', 'fusca', 'celta']
print(cars)

#organiza temporariamente em ordem alfabética
print (sorted(cars))

#organiza permanentemente em ordem alfabética
cars.sort()
print (cars)

#inverte a ordem da lista
cars.reverse()
print(cars)

#conta quantos elementos tem uma lista
print(len(cars))